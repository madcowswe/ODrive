class Mapping():
    def __init__(self, name, datatype, idnumber, access, min, max):
        self.name = name
        self.datatype = datatype # 0:float ; 1:int ; 2:bool
        self.idnumber = idnumber
        self.access = access
        self.min = min
        self.max = max
        if self.name.find(".") == -1:
            self.scope = "global"
        elif self.name.find("motors[") == 0:
            if self.name.find(".rotor.") != -1:
                self.scope = "rotor"
            elif self.name.find(".current_control.") != -1:
                self.scope = "current_control"
            else:
                self.scope = "motor"
        else:
            print("Unknown scope for %s, fix generator code!" % self.name)
      

