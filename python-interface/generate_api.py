template_get_monitor = '''
    def get_%(functionName)s(self):
        mapping = variable_map[%(mappingKey)s]
        return self._get(mapping.datatype, mapping.idnumber)

    def monitor_%(functionName)s(self, frequency):
        mapping = variable_map[%(mappingKey)s]
        self._monitor(mapping.datatype, mapping.idnumber, frequency)
'''
template_set = '''
    def set_%(functionName)s(self, value):
        mapping = variable_map[%(mappingKey)s]
        if mapping.datatype == 2:
            value = 0 if value == False else 1
        self._set(mapping.datatype, mapping.idnumber, value)
'''


